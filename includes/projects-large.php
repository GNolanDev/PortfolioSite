                    Timerthing is a public website I built from scratch to solve the problem of recording and storing times for a volunteer-led local running group following the closure of the national organisation which previously supported it. Users sign up and get a personal QR code, which event organisers can then scan in using an event timing page of the same application. Names are fetched from the server during the live event; multiple devices can be used for timing the same event; unknown finishers can be added, along with temporary names which users can 'claim' later; emergency contact info can optionally be added which can be made available to organisers or even just anyone with a smart phone since the QR code represents a real URL. Anyone with an account can set up their own location, with multiple events at each location, all of which can be run simultaneously in separate tabs. HTML, CSS, & Javascript is used for the front end & most of the actual timing functionality. JSON is used for communicating with the API created on the server side in PHP, with live events and long term storage, user account info etc all handled in MySQL (MariaDB during local development on XAMPP server). Hosted on Hostinger.com
